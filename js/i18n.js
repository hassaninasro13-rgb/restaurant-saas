/**
 * @param {string} key
 * @param {Record<string, string | number>} [params] placeholders {name} in string
 */
export function t(key, params = {}) {
  const table = MESSAGES[activeLang] || MESSAGES.fr;
  const fallback = MESSAGES.fr;
  let str = table[key] ?? fallback[key] ?? key;
  if (params && typeof str === 'string') {
    Object.keys(params).forEach((k) => {
      str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), String(params[k]));
    });
  }
  return str;
}

/** Fill [data-i18n], [data-i18n-placeholder], [data-i18n-aria] inside root. */
export function hydrateI18n(root = document) {
  root.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (!key) return;
    el.textContent = t(key);
  });
  root.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (!key || !('placeholder' in el)) return;
    el.placeholder = t(key);
  });
  root.querySelectorAll('[data-i18n-aria]').forEach((el) => {
    const key = el.getAttribute('data-i18n-aria');
    if (!key) return;
    el.setAttribute('aria-label', t(key));
  });
}
