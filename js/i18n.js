/**
 * Simple dictionary i18n (fr / en / ar).
 * Active language is set from restaurant_settings via setI18nLanguage().
 */

let activeLang = 'fr';

/** @param {string} [raw] */
export function normalizeLang(raw) {
  const x = String(raw || 'fr').toLowerCase().trim().slice(0, 12);
  if (x.startsWith('ar')) return 'ar';
  if (x.startsWith('en')) return 'en';
  return 'fr';
}

/** @param {string} [raw] from restaurant_settings.language */
export function setI18nLanguage(raw) {
  activeLang = normalizeLang(raw);
  document.documentElement.lang = activeLang === 'ar' ? 'ar' : activeLang === 'en' ? 'en' : 'fr';
  document.documentElement.dir = activeLang === 'ar' ? 'rtl' : 'ltr';
}

export function getActiveLang() {
  return activeLang;
}

